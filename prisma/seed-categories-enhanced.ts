async function seedCategory(
  categoryData: CategoryData,
  parentId: number | null = null,
  order: number = 0
) {
  console.log(`Creating category: ${categoryData.nameFr} (parent: ${parentId})`);

  const category = await prisma.category.create({
    data: {
      slug: categoryData.slug,
      name: categoryData.name,
      nameFr: categoryData.nameFr,
      nameEn: categoryData.nameEn,
      nameDe: categoryData.nameDe,
      description: categoryData.description,
      icon: categoryData.icon,
      color: categoryData.color,
      parentId: parentId,
      googleTypes: categoryData.googleTypes || [],
      googleBusinessCategory: categoryData.googleBusinessCategory,
      isActive: true,
      order: order,
    },
  });

  // Recursively create children
  if (categoryData.children) {
    for (let i = 0; i < categoryData.children.length; i++) {
      await seedCategory(categoryData.children[i], category.id, i);
    }
  }

  return category;
}

